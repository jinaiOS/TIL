package chapter_13;

public class IntegerRange {
    public static void main(String[] args) {
        // Byte 범위: -128 ~ 127
        // Short 범위: -32768 ~ 32767
        byte myByte = 120;
        short myShort = 23312;
        long myLong = 12232323344340L;
        System.out.println(myByte);
        System.out.println(myShort);
        System.out.println(myLong);
    }
}
